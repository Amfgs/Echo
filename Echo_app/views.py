def registrar(request):
    """
    Renderiza a p치gina de registro e processa a cria칞칚o de um novo usu치rio
    usando o NOME DE USU츼RIO fornecido no formul치rio.
    """
    contexto = {'erros': [], 'dados_preenchidos': {}} 
    
    try:
        contexto['todas_categorias'] = Categoria.objects.all()
    except:
        contexto['todas_categorias'] = []
    
    if request.method == "POST":
        # 2. Obter dados crus do POST
        username = request.POST.get('username') # <-- MUDAN칂A: Lendo o username
        email = request.POST.get('email')
        password = request.POST.get('password')
        password_confirm = request.POST.get('password_confirm')
        categorias_selecionadas_ids = request.POST.getlist('categoria') 

        contexto['dados_preenchidos'] = {
            'username': username, # <-- MUDAN칂A: Repassando o username
            'email': email,
            'categorias_selecionadas_ids': categorias_selecionadas_ids, 
        }

        # 3. Gera칞칚o de Username REMOVIDA

        # 4. Valida칞칚o manual
        if not username or not email or not password or not password_confirm: # <-- MUDAN칂A
            contexto['erros'].append('Todos os campos obrigat칩rios devem ser preenchidos: Nome de Usu치rio, Email e Senha.')
        
        if password != password_confirm:
            contexto['erros'].append('As senhas n칚o coincidem.')
        
        # A gera칞칚o autom치tica de username foi removida.
        # Agora, se o username j치 existe, apenas informamos o erro.
        if username and User.objects.filter(username__iexact=username).exists(): # <-- MUDAN칂A
            contexto['erros'].append('Este nome de usu치rio j치 est치 em uso. Por favor, escolha outro.')
        
        if email and User.objects.filter(email__iexact=email).exists():
            contexto['erros'].append('Este e-mail j치 est치 cadastrado.')

        # 5. Se n칚o houver erros, criar o usu치rio
        if not contexto['erros']:
            try:
                user = User.objects.create_user(
                    username=username, # <-- MUDAN칂A: Usando o username do formul치rio
                    email=email,
                    password=password
                    # NOTA: O campo 'first_name' n칚o est치 mais sendo salvo aqui.
                    # Se voc칡 quiser salvar o "Nome completo" tamb칠m,
                    # precisar치 adicionar um novo campo no registrar.html
                )
                
                # 6. Salvar as categorias no PerfilUsuario
                if categorias_selecionadas_ids:
                    categorias = Categoria.objects.filter(pk__in=categorias_selecionadas_ids)
                    perfil, created = PerfilUsuario.objects.get_or_create(usuario=user)
                    perfil.categorias_de_interesse.set(categorias)
                
                # 7. Logar o usu치rio automaticamente
                login(request, user)
                return redirect("dashboard")
                
            except IntegrityError:
                contexto['erros'].append('Erro ao criar usu치rio. Tente novamente.')
            except Exception as e:
                contexto['erros'].append(f'Ocorreu um erro: {e}')

    return render(request, "Echo_app/registrar.html", contexto)


